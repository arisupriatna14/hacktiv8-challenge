function graduates (students) {
  var isiObj = {}

  for(var a = 0; a < students.length; a++){
    if(isiObj[students[a].class] === undefined){
      isiObj[students[a].class] = []
    }

    if(students[a].score > 75){
      isiObj[students[a].class].push({name: students[a].name, score: students[a].score})
    }
    

  }
  return isiObj
}
  
